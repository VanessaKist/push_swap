#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*aux;

	if (c > 255)
		return ((void *)s);
	aux = (char *)s;
	while (*aux)
	{
		if (*aux == c)
			return (aux);
		aux++;
	}
	if (*aux == c)
		return (aux);
	return (NULL);
}
